const fs = require('fs');
const path = require('path');
const assert = require('assert');
const root = process.cwd();
const read = file => fs.readFileSync(path.join(root, file), 'utf8');
const exists = file => fs.existsSync(path.join(root, file));
const localPath = value => value.replace(/^\.\//, '').split(/[?#]/)[0];
const index = read('index.html');
const manifest = JSON.parse(read('manifest.webmanifest'));
const serviceWorker = read('sw.js');
const indexAssets = [...index.matchAll(/(?:src|href)="([^"]+)"/g)].map(match => match[1]).filter(value => !/^(?:https?:|data:|#)/.test(value)).map(localPath);
const cacheBlock = serviceWorker.match(/const ASSETS = \[([\s\S]*?)\];/);
assert.ok(cacheBlock, 'No se localiza ASSETS en sw.js');
const cachedAssets = [...cacheBlock[1].matchAll(/['"]([^'"]+)['"]/g)].map(match => localPath(match[1]));
for (const asset of indexAssets) assert.ok(exists(asset), `Recurso de index inexistente: ${asset}`);
for (const asset of cachedAssets) { if (!asset || asset === '.') continue; assert.ok(exists(asset), `Recurso de PWA inexistente: ${asset}`); }
for (const asset of indexAssets.filter(item => /\.(?:js|css|webmanifest|svg)$/.test(item))) assert.ok(cachedAssets.includes(asset), `Recurso de index no incluido en caché: ${asset}`);
assert.equal(new Set(cachedAssets).size, cachedAssets.length, 'Hay recursos duplicados en ASSETS');
assert.ok(/const CACHE = 'opoweb-v85';/.test(serviceWorker), 'La caché no es opoweb-v85');
assert.ok(index.includes("navigator.serviceWorker.register('./sw.js')"));
assert.equal(manifest.start_url, './index.html'); assert.equal(manifest.display, 'standalone');
for (const icon of manifest.icons) assert.ok(exists(localPath(icon.src)), `Icono inexistente: ${icon.src}`);
assert.ok(index.includes('assets/js/uc3m-v78-presupuesto-2026.js'));
assert.ok(index.includes('assets/js/ui-v78.js'));
console.log(`Integridad web v0.78 OK · ${indexAssets.length} referencias · ${cachedAssets.length} recursos PWA`);
